def parse_training_data(log_text):
    """Parse the training log to extract timestep and PSNR values."""
    timesteps = []
    psnr_values = []

    # Regular expression to extract timestep and PSNR
    pattern = r'timestep (\d+):.*?PSNR=([0-9.]+)'

    matches = re.findall(pattern, log_text)

    for match in matches:
        timestep = int(match[0])
        psnr = float(match[1])
        timesteps.append(timestep)
        psnr_values.append(psnr)

    return timesteps, psnr_values


